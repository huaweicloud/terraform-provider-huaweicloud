func buildJobsQueryParams(d *schema.ResourceData) string {
	queryParams := ""

	if v, ok := d.GetOk("job_id"); ok {
		queryParams = fmt.Sprintf("%s&id=%v", queryParams, v)
	}
	if v, ok := d.GetOk("name"); ok {
		queryParams = fmt.Sprintf("%s&name=%v", queryParams, v)
	}
	if v, ok := d.GetOk("status"); ok {
		queryParams = fmt.Sprintf("%s&status=%v", queryParams, v)
	}
	if v, ok := d.GetOk("start_time"); ok {
		queryParams = fmt.Sprintf("%s&start_time=%v", queryParams, v)
	}
	if v, ok := d.GetOk("end_time"); ok {
		queryParams = fmt.Sprintf("%s&end_time=%v", queryParams, v)
	}

	return queryParams
}

func dataSourceJobsRead(_ context.Context, d *schema.ResourceData, meta interface{}) diag.Diagnostics {
	var (
		cfg     = meta.(*config.Config)
		region  = cfg.GetRegion(d)
		httpUrl = "v3/{project_id}/jobs?limit={limit}"
		offset  = 0
		limit   = 50
		result  = make([]interface{}, 0)
	)

	client, err := cfg.NewServiceClient("geminidb", region)
	if err != nil {
		return diag.Errorf("error creating GeminiDB client: %s", err)
	}

	getPath := client.Endpoint + httpUrl
	getPath = strings.ReplaceAll(getPath, "{project_id}", client.ProjectID)
	getPath = strings.ReplaceAll(getPath, "{limit}", strconv.Itoa(limit))
	getPath += buildJobsQueryParams(d)
	getOpt := golangsdk.RequestOpts{
		MoreHeaders: map[string]string{
			"Content-Type": "application/json",
		},
		KeepResponseBody: true,
	}

	for {
		currentPath := fmt.Sprintf("%s&offset=%v", getPath, offset)
		getResp, err := client.Request("GET", currentPath, &getOpt)
		if err != nil {
			return diag.Errorf("error retrieving the GeminiDB tasks: %s", err)
		}

		getRespBody, err := utils.FlattenResponse(getResp)
		if err != nil {
			return diag.FromErr(err)
		}

		jobs := utils.PathSearch("jobs", getRespBody, make([]interface{}, 0)).([]interface{})
		result = append(result, jobs...)
		if len(jobs) < limit {
			break
		}

		offset += len(jobs)
	}

	randomUUID, err := uuid.NewRandom()
	if err != nil {
		return diag.Errorf("unable to generate ID: %s", err)
	}

	d.SetId(randomUUID.String())

	mErr := multierror.Append(
		d.Set("region", region),
		d.Set("jobs", flattenJobs(result)),
	)

	return diag.FromErr(mErr.ErrorOrNil())
}

func flattenJobs(resp []interface{}) []interface{} {
	if len(resp) == 0 {
		return nil
	}

	result := make([]interface{}, 0, len(resp))
	for _, v := range resp {
		result = append(result, map[string]interface{}{
			"id":          utils.PathSearch("id", v, nil),
			"name":        utils.PathSearch("name", v, nil),
			"status":      utils.PathSearch("status", v, nil),
			"start_time":  utils.PathSearch("start_time", v, nil),
			"end_time":    utils.PathSearch("end_time", v, nil),
			"progress":    utils.PathSearch("progress", v, nil),
			"instance":    flattenJobInstanceInfo(utils.PathSearch("instance", v, nil)),
			"fail_reason": utils.PathSearch("fail_reason", v, nil),
		})
	}

	return result
}

func flattenJobInstanceInfo(resp interface{}) []interface{} {
	if resp == nil {
		return nil
	}

	result := map[string]interface{}{
		"id":   utils.PathSearch("id", resp, nil),
		"name": utils.PathSearch("name", resp, nil),
	}

	return []interface{}{result}
}
