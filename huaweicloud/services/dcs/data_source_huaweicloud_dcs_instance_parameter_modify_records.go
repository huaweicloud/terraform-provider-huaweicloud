// @API DCS GET /v2/{project_id}/instances/{instance_id}/config-histories
func DataSourceInstanceParameterModifyRecords() *schema.Resource {
	return &schema.Resource{
		ReadContext: dataSourceInstanceParameterModifyRecordsRead,

		Schema: map[string]*schema.Schema{
			"region": {
				Type:     schema.TypeString,
				Optional: true,
				Computed: true,
			},
			"instance_id": {
				Type:     schema.TypeString,
				Required: true,
			},
			"histories": {
				Type:     schema.TypeList,
				Computed: true,
				Elem:     instanceParameterModifyRecordsHistorySchema(),
			},
		},
	}
}

func instanceParameterModifyRecordsHistorySchema() *schema.Resource {
	return &schema.Resource{
		Schema: map[string]*schema.Schema{
			"history_id": {
				Type:     schema.TypeString,
				Computed: true,
			},
			"type": {
				Type:     schema.TypeString,
				Computed: true,
			},
			"created_at": {
				Type:     schema.TypeString,
				Computed: true,
			},
			"status": {
				Type:     schema.TypeString,
				Computed: true,
			},
		},
	}
}

func dataSourceInstanceParameterModifyRecordsRead(_ context.Context, d *schema.ResourceData, meta interface{}) diag.Diagnostics {
	cfg := meta.(*config.Config)
	region := cfg.GetRegion(d)

	var mErr *multierror.Error

	client, err := cfg.NewServiceClient("dcs", region)
	if err != nil {
		return diag.Errorf("error creating DCS client: %s", err)
	}

	httpUrl := "v2/{project_id}/instances/{instance_id}/config-histories"
	listPath := client.Endpoint + httpUrl
	listPath = strings.ReplaceAll(listPath, "{project_id}", client.ProjectID)
	listPath = strings.ReplaceAll(listPath, "{instance_id}", d.Get("instance_id").(string))

	listResp, err := pagination.ListAllItems(
		client,
		"offset",
		listPath,
		&pagination.QueryOpts{MarkerField: ""})
	if err != nil {
		return diag.Errorf("error retrieving DCS instance parameter modify records: %s", err)
	}

	listRespJson, err := json.Marshal(listResp)
	if err != nil {
		return diag.FromErr(err)
	}

	var listRespBody interface{}
	err = json.Unmarshal(listRespJson, &listRespBody)
	if err != nil {
		return diag.FromErr(err)
	}

	id, err := uuid.GenerateUUID()
	if err != nil {
		return diag.Errorf("unable to generate ID: %s", err)
	}
	d.SetId(id)

	mErr = multierror.Append(
		d.Set("region", region),
		d.Set("histories", flattenListInstanceParameterModifyRecordsBody(listRespBody)),
	)

	return diag.FromErr(mErr.ErrorOrNil())
}

func flattenListInstanceParameterModifyRecordsBody(resp interface{}) []interface{} {
	curJson := utils.PathSearch("histories", resp, make([]interface{}, 0))
	curArray := curJson.([]interface{})
	res := make([]interface{}, 0, len(curArray))
	for _, v := range curArray {
		res = append(res, map[string]interface{}{
			"history_id": utils.PathSearch("history_id", v, nil),
			"type":       utils.PathSearch("type", v, nil),
			"created_at": utils.PathSearch("created_at", v, nil),
			"status":     utils.PathSearch("status", v, nil),
		})
	}
	return res
}
